"""
spectangle.data_loaders.dataset
================================
PyTorch Dataset and DataModule for HDF5 simulation files generated by
``SimpleSimulator`` and ``ComplexSimulator``.

Dataset format
--------------
Each HDF5 file contains:

    /metadata          — simulation config as attributes (includes pad_y, pad_x,
                         image_shape_ny/nx, spectrogram_shape_ny/nx, n_spectral_pixels)
    /wavelengths       — (n_lambda,) float32
    /samples/
        /0000/
            cube               — (n_lambda, ny, nx)              float32
            spectrograms       — (4, ny+2·pad_y, nx+2·pad_x)    float32
            spectrograms_clean — (4, ...)                        float32  [complex only]
            noise_maps         — (4, ...)                        float32  [complex only]
            direct_image       — (ny+2·pad_y, nx+2·pad_x)       float32  [optional]
            source_xs, source_ys, source_temps                   float32
        /0001/ ...

SpectangleDataset
-----------------
Returns (x, y) pairs where:
  x — Tensor, shape (C_in, ny+2·pad_y, nx+2·pad_x)
      C_in = 4  → spectrograms only
      C_in = 5  → spectrograms + direct_image (appended as channel 5)
  y — Tensor, shape (n_lambda, ny, nx)  — ground-truth cube

Normalisation
-------------
``per_sample``  — each sample is normalised by its own 99th-percentile flux.
``global``      — min-max over the dataset (computed lazily on first access).
``none``        — no normalisation.
"""

from __future__ import annotations

import math
from pathlib import Path
from typing import Dict, List, Optional, Tuple

import h5py
import numpy as np
import torch
from torch.utils.data import DataLoader, Dataset, Subset, random_split


# ---------------------------------------------------------------------------
# Per-sample normalisation helpers
# ---------------------------------------------------------------------------

def _normalise_per_sample(arr: np.ndarray, eps: float = 1e-8) -> np.ndarray:
    """Divide by 99th-percentile flux (makes all samples comparable scale)."""
    p99 = np.percentile(arr, 99)
    return arr / (p99 + eps)


# ---------------------------------------------------------------------------
# SpectangleDataset
# ---------------------------------------------------------------------------

class SpectangleDataset(Dataset):
    """PyTorch Dataset backed by a single HDF5 simulation file.

    Parameters
    ----------
    h5_path : str or Path
        Path to the ``.h5`` simulation file.
    n_channels : int
        4 → spectrograms only;
        5 → spectrograms + direct_image (concatenated along channel axis).
    normalise : str
        One of ``"per_sample"``, ``"none"``.
    indices : list of int, optional
        Subset of sample indices to expose (used for train/val/test splits).
    """

    def __init__(
        self,
        h5_path: str | Path,
        n_channels: int = 4,
        normalise: str = "per_sample",
        indices: Optional[List[int]] = None,
    ) -> None:
        self.h5_path   = Path(h5_path)
        self.n_channels = n_channels
        self.normalise  = normalise

        # Read metadata and sample count directly from the HDF5 file without
        # loading any data arrays.  This keeps memory usage O(1) regardless
        # of dataset size.
        with h5py.File(self.h5_path, "r") as f:
            self._all_keys: List[str] = sorted(f["samples"].keys())
            meta = {k: v for k, v in f["metadata"].attrs.items()} if "metadata" in f else {}
            self._wavelengths = f["wavelengths"][:] if "wavelengths" in f else None

        # Parse geometry from metadata (with safe fallbacks)
        self._metadata = meta
        self.pad_y = int(float(meta.get("pad_y", 0)))
        self.pad_x = int(float(meta.get("pad_x", 0)))
        self.n_lambda = int(float(meta.get("n_spectral_pixels", 128)))

        _ny = int(float(meta.get("image_shape_ny", 128)))
        _nx = int(float(meta.get("image_shape_nx", 128)))
        self.scene_shape: Tuple[int, int] = (_ny, _nx)

        _sy = int(float(meta.get("spectrogram_shape_ny", _ny + 2 * self.pad_y)))
        _sx = int(float(meta.get("spectrogram_shape_nx", _nx + 2 * self.pad_x)))
        self.spectrogram_shape: Tuple[int, int] = (_sy, _sx)

        # Restrict to requested indices (for splits)
        if indices is not None:
            self._keys = [self._all_keys[i] for i in indices]
        else:
            self._keys = self._all_keys

    # ------------------------------------------------------------------

    def __len__(self) -> int:
        return len(self._keys)

    def __getitem__(self, idx: int) -> Tuple[torch.Tensor, torch.Tensor]:
        key = self._keys[idx]
        with h5py.File(self.h5_path, "r") as f:
            grp  = f["samples"][key]
            spec = grp["spectrograms"][:].astype(np.float32)   # (4, Hy, Hx)
            cube = grp["cube"][:].astype(np.float32)            # (n_lambda, ny, nx)
            direct = grp["direct_image"][:] if "direct_image" in grp else None

        # Optionally append direct image as 5th channel
        if self.n_channels >= 5 and direct is not None:
            x = np.concatenate(
                [spec, direct.astype(np.float32)[None, ...]],
                axis=0,
            )  # (5, Hy, Hx)
        else:
            x = spec  # (4, Hy, Hx)

        # Normalise
        if self.normalise == "per_sample":
            x    = _normalise_per_sample(x)
            cube = _normalise_per_sample(cube)

        return torch.from_numpy(x), torch.from_numpy(cube)

    # ------------------------------------------------------------------

    @property
    def wavelengths(self) -> Optional[np.ndarray]:
        """Wavelength grid in ångströms, shape (n_lambda,) or None."""
        return self._wavelengths

    @property
    def metadata(self) -> Dict:
        return self._metadata

    def __repr__(self) -> str:
        return (
            f"SpectangleDataset(n={len(self)}, "
            f"channels={self.n_channels}, "
            f"scene={self.scene_shape}, "
            f"spec={self.spectrogram_shape}, "
            f"n_lambda={self.n_lambda})"
        )


# ---------------------------------------------------------------------------
# SpectangleDataModule
# ---------------------------------------------------------------------------

class SpectangleDataModule:
    """Convenience class that creates train/val/test DataLoaders.

    Parameters
    ----------
    h5_path : str or Path
    split_ratios : tuple of float
        (train, val, test) fractions summing to 1.
    batch_size : int
    n_channels : int
    normalise : str
    num_workers : int
    seed : int
        RNG seed for reproducible splits.
    """

    def __init__(
        self,
        h5_path: str | Path,
        split_ratios: Tuple[float, float, float] = (0.8, 0.1, 0.1),
        batch_size: int = 16,
        n_channels: int = 4,
        normalise: str = "per_sample",
        num_workers: int = 4,
        seed: int = 42,
    ) -> None:
        self.h5_path      = Path(h5_path)
        self.split_ratios = split_ratios
        self.batch_size   = batch_size
        self.n_channels   = n_channels
        self.normalise    = normalise
        self.num_workers  = num_workers
        self.seed         = seed

        # Read total sample count
        with h5py.File(self.h5_path, "r") as f:
            n_total = len(f["samples"])

        # Compute split sizes
        n_train = int(math.floor(n_total * split_ratios[0]))
        n_val   = int(math.floor(n_total * split_ratios[1]))
        n_test  = n_total - n_train - n_val

        # Reproducible shuffle
        rng   = np.random.default_rng(seed)
        order = rng.permutation(n_total).tolist()

        train_idx = order[:n_train]
        val_idx   = order[n_train: n_train + n_val]
        test_idx  = order[n_train + n_val:]

        common = dict(h5_path=h5_path, n_channels=n_channels, normalise=normalise)
        self._train_ds = SpectangleDataset(indices=train_idx, **common)
        self._val_ds   = SpectangleDataset(indices=val_idx,   **common)
        self._test_ds  = SpectangleDataset(indices=test_idx,  **common)

        # Expose geometry properties (read from one dataset)
        self.scene_shape:       Optional[Tuple[int, int]] = self._train_ds.scene_shape
        self.spectrogram_shape: Optional[Tuple[int, int]] = self._train_ds.spectrogram_shape
        self.n_lambda:          Optional[int]             = self._train_ds.n_lambda
        self.pad_y:             int                       = self._train_ds.pad_y
        self.pad_x:             int                       = self._train_ds.pad_x

    # ------------------------------------------------------------------

    def _loader(self, dataset: SpectangleDataset, shuffle: bool) -> DataLoader:
        return DataLoader(
            dataset,
            batch_size=self.batch_size,
            shuffle=shuffle,
            num_workers=self.num_workers,
            pin_memory=True,
            drop_last=False,
        )

    def train_dataloader(self) -> DataLoader:
        return self._loader(self._train_ds, shuffle=True)

    def val_dataloader(self) -> DataLoader:
        return self._loader(self._val_ds, shuffle=False)

    def test_dataloader(self) -> DataLoader:
        return self._loader(self._test_ds, shuffle=False)

    def __repr__(self) -> str:
        return (
            f"SpectangleDataModule(\n"
            f"  file         = {self.h5_path}\n"
            f"  train/val/test = {len(self._train_ds)}/{len(self._val_ds)}/{len(self._test_ds)}\n"
            f"  batch_size   = {self.batch_size}\n"
            f"  channels     = {self.n_channels}\n"
            f"  scene_shape  = {self.scene_shape}\n"
            f"  spec_shape   = {self.spectrogram_shape}\n"
            f"  n_lambda     = {self.n_lambda}\n"
            f")"
        )
